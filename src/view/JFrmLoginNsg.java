    private void jBtnEntrar_NsgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEntrar_NsgActionPerformed
        // TODO add your handling code here:
        String usuario = jTxtUsuario_Nsg.getText();
        String senha = new String(jPwfSenha_Nsg.getPassword());
        Usuarios_DAO_Nsg usuarios_DAO_Nsg = new Usuarios_DAO_Nsg();
        
        if(usuarios_DAO_Nsg.Login(usuario, senha) != null){
            JFrmPrincipalNsg.setVisible(true);
        }else{
            Util.mensagem("Por favor, preencha os campos corretamente!");
        }
    }//GEN-LAST:event_jBtnEntrar_NsgActionPerformed

